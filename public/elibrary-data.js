function elibThumbUrl(doc) {
  if (doc.archiveId) {
    return 'https://archive.org/services/img/' + doc.archiveId;
  }
  return null;
}

function elibEmbedUrl(doc) {
  if (doc.archiveId && doc.hasPdf) {
    return 'https://archive.org/embed/' + doc.archiveId + '?autoplay=0';
  }
  return null;
}

/** Same-origin PDF stream (requires Node server with /api/elibrary/stream) */
function elibStreamUrl(pdfUrl) {
  if (!pdfUrl) return null;
  return '/api/elibrary/stream?url=' + encodeURIComponent(pdfUrl);
}

/** Direct Archive PDF — works without API; used as fallback */
function elibDirectPdfUrl(doc) {
  return doc.hasPdf && doc.downloadUrl ? doc.downloadUrl : null;
}

/** Prefer proxy when server supports it; caller may fall back to direct */
function elibInSitePdfUrl(doc, useProxy) {
  if (!doc.hasPdf || !doc.downloadUrl) return null;
  if (useProxy !== false) {
    return elibStreamUrl(doc.downloadUrl);
  }
  return doc.downloadUrl;
}

function getElibDocById(id) {
  return ELIB_DOCUMENTS.find(function (d) { return d.id === id; });
}

function getElibNiimhById(id) {
  return ELIB_NIIMH_REFERENCES.find(function (r) { return r.id === id; });
}
